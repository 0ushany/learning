# 放眼世界

world = ['china', 'tokiyo', 'hongkong', 'american', 'british']
print(world)

print("-------------------------------------------------------------")

# sorted排序
print(sorted(world))
print(world)

print("-------------------------------------------------------------")

# reverse()修改元素顺序
world.reverse()
print(world)
world.reverse()
print(world)

print("-------------------------------------------------------------")

# sort()修改元素排序
world.sort()
print(world)

# 字母顺序相反
world.sort(reverse = True)
print(world)
