# 人
# 把字典嵌套到列表里面

osy = {'first_name':'sy', 'last_name':'o', 'age':18, 'city':'qinzhou'}
oshany = {'first_name':'shany', 'last_name':'o', 'age':22, 'city':'shenzhen'}
superman = {'first_name':'clark', 'last_name':'man', 'age':25, 'city':'newyork'}

people = [osy, oshany, superman] 

for peo in people:
    print(peo)

