# 切片
pizzas = ['apple', 'banana', 'pear', 'orange', 'lemon']

print("The first three items in the list are: ")
print(pizzas[0:3])

print("----------------------------------------------------")

print("Three items from the middle of the list are: ")
print(pizzas[1:4])
# 1:4意思是从索引1到索引3的元素，不包括索引4的

print("----------------------------------------------------")
print(pizzas[-3:])
# -3，从末尾开始3个的意思，不需要end_index

